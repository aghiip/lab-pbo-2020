/*
 Program berikut untuk mendapatkan nilai argumen pada method main
 */
 
public class GetArguments {

    public static void main(String args[]) {
    	/*Sebelum memulai program masukkan argumen Tanggal, Bulan, Tahun*/
        System.out.println("Tanggal : " + args[0]); /* Disini akan menyimpan Tanggal yaitu 23*/
        System.out.println("Bulan : " + args[1]); /* Disisni akan menyimpan Bulan yaitu  maret*/
        System.out.println("Tahun : " + args[2]); /* Disini akan menyimpan Tahun yaitu 2020*/

    }

}