/* 
 Program berikut untuk mencoba seleksi if-else
 */

public class IfElseExample {	

	public static void main(String[] args) {
		int number=13;
		if(number%2==0) { 
			System.out.println("Angka genap"); /* operator if yang apabila sisa dari pembagian antara nilai number dengan 2 adalah 0, maka akan menjalankan perintah print Angka genap*/
		}else{ 
			System.out.println("Angka ganjil"); /*operator else, apabila sisa dari pembagian antara nilai number dengan 2 selain 0, maka akan menjalankan perintah print Angka ganjil*/

		}

	}

}

 /* karena disini menggunakan number=13, maka akan menampilkan Angka ganjil*/